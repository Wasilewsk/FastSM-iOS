import UserNotifications
import shared

class NotificationService: ObservableObject {
    static let shared = NotificationService()
    
    func requestPermission() {
        UNUserNotificationCenter.current().requestAuthorization(options: [.alert, .badge, .sound]) { granted, error in
            if let error = error {
                print("Notification permission error: \(error)")
            }
        }
    }
    
    func schedulePolling() {
        let content = UNMutableNotificationContent()
        content.title = "FastSM"
        content.body = "Checking for new notifications..."
        
        let trigger = UNTimeIntervalNotificationTrigger(timeInterval: 900, repeats: true)
        let request = UNNotificationRequest(identifier: "fastsm-poll", content: content, trigger: trigger)
        
        UNUserNotificationCenter.current().add(request) { error in
            if let error = error {
                print("Failed to schedule notification: \(error)")
            }
        }
    }
    
    func showNotification(title: String, body: String) {
        let content = UNMutableNotificationContent()
        content.title = title
        content.body = body
        content.sound = .default
        
        let request = UNNotificationRequest(
            identifier: UUID().uuidString,
            content: content,
            trigger: nil
        )
        
        UNUserNotificationCenter.current().add(request)
    }
}
